"""Take two numbers from the user using input(). 
Convert them to floats and print their sum, difference, product, and quotient — each on a separate line."""
print("Enter 1st user value:" ) 
user_1 = int(input())
print("Enter 2nd user value:" )
user_2 = int(input())

print("converted float value and their sum is:", float(user_1 + user_2))
print("converted float value and their difference is:", float(user_1 - user_2))
print("converted float value and their product is:", float(user_1 * user_2))
print("converted float value and their quotient is:", float(user_1 / user_2))

"""Q9. Create a variable score = 0. 
Add 10 to it, then multiply it by 2, then subtract 5 — each as a separate step. Print the value after each operation."""

score = 0
print(f"Score is {score}")
add_score = score + 10
print(f"Adding 10 to it: {add_score}")
multiply_score = add_score * 2
print(f"then multiply it by 2, and the value is: {multiply_score}")
subtract_score = multiply_score - 5
print(f"then subtract 5 = {subtract_score}")
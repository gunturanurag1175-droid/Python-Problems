"""Store a = 17 and b = 5. Print the result of regular division / and floor division //. 
Explain in a comment what the difference is."""

a = 17
b = 5
div = a / b # -> This is a regular division, which will also give the decimal values
floorDiv = a // b # -> This is floor division, which removes the decimal part and gives the lower whole number.
print(f"Regular Division: {div}\nFloor Division: {floorDiv}")

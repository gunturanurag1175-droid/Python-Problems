# TO FIND THE LENGTH OF THE STRING WE USE THE 'LEN' KEYWORD. EXAMPLE IS BELOW:

course = 'Python for BegiNners'
print(len(course))

# TO MODIFY THE STRING STATEMENT WE CAN USE MANY METHODS, AND THE METHODS ARE: 
print(course.upper()) # which converts the string statement to Uppercase letters
print(course.lower()) # which converts the string statement to lowercase letters
print(course.find('N'))# which finds a specific string index of the letter from the string statement.
print(course.replace('BegiNners', 'Absolute beginners'))# which replaces a specific string of the letter from the string statement.
print('Python' in course)# This method checks if the word/string exists in the string statement or not (True/FaLse)